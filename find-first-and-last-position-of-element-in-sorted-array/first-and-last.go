func searchRange(nums []int, target int) []int {
	if len(nums) == 0 {
		return []int{-1, -1}
	}

	left := -1
	right := -1

	for i := 0; i < len(nums); i++ {
		if target < nums[i] {
			return []int{left, right}
		}

		if nums[i] == target {
			if left == -1 {
				left = i
			}

			if right == -1 {
				right = i
			}

			if right != -1 && target == nums[i]{
				right = i
			}
		}
	}

	return []int{left, right}
}
